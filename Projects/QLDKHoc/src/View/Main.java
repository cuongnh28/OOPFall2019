    public MonHoc getMHbyMa(int maM) {
        for (int i = 0; i < tmM.getRowCount(); i++) {
            if (Integer.parseInt(tmM.getValueAt(i, 0).toString()) == maM) {
                return new MonHoc(maM, tmM.getValueAt(i, 1).toString(),
                        Integer.parseInt(tmM.getValueAt(i, 2).toString()),
                        tmM.getValueAt(i, 3).toString());
            }
        }
        return null;
    }
    
    public SinhVien getSVbyMa(int maS) {
        for (int i = 0; i < tmS.getRowCount(); i++) {
            if (Integer.parseInt(tmS.getValueAt(i, 0).toString()) == maS) {
                return new SinhVien(maS, tmS.getValueAt(i, 1).toString(),
                        tmS.getValueAt(i, 2).toString(),
                        tmS.getValueAt(i, 3).toString());
            }
        }
        return null;
    }
    
    public void loadMH(){
        List<MonHoc> list = new ArrayList<>();
        IOFile.readFile(list, fileM);
        tmM.setRowCount(0);
        for(MonHoc i:list){
            tmM.addRow(i.toObject());
        }
    }
    
    public void loadSV(){
        List<SinhVien> list = new ArrayList<>();
        IOFile.readFile(list, fileS);
        tmS.setRowCount(0);
        for(SinhVien i:list){
            tmS.addRow(i.toObject());
        }
    }
    
    public void loadQL(){
        List<QLDK> list = new ArrayList<>();
        IOFile.readFile(list, fileQL);
        tmQL.setRowCount(0);
        for(QLDK i:list){
            String tenMH=getMHbyMa(i.getMaM()).getTen(),
                   tenSV=getSVbyMa(i.getMaS()).getTen();
            tmQL.addRow(new Object[] {i.getMaM(),tenMH,i.getMaS(),tenSV,i.getDate()});
        }
    }
    
    public boolean tonTai(int maM,int maS){
        for(int i=0;i<tmQL.getRowCount();i++){
            int maMx=Integer.parseInt(tmQL.getValueAt(i, 0).toString()),
                maSx=Integer.parseInt(tmQL.getValueAt(i, 2).toString());
            if(maMx==maM && maSx==maS){
                return true;
            }
        }
        return false;
    }
    
    public int getSoMH(int maS){
        int c=0;
        for(int i=0;i<tmQL.getRowCount();i++){
            int maSx=Integer.parseInt(tmQL.getValueAt(i, 2).toString());
            if(maS==maSx){
                c++;
            }
        }
        return c;
    }
    
    public void comboSV(){
        jComboBox3.removeAllItems();
        for(int i=0;i<tmS.getRowCount();i++){
            jComboBox3.addItem(tmS.getValueAt(i, 0).toString());
        }
    }
    
    public void comboMH(){
        jComboBox2.removeAllItems();
        for(int i=0;i<tmM.getRowCount();i++){
            jComboBox2.addItem(tmM.getValueAt(i, 0).toString());
        }
    }
    
    public List<QLDK> getALLDK(){
        List<QLDK> list = new ArrayList<>();
        for(int i=0;i<tmQL.getRowCount();i++){
            QLDK m = new QLDK(Integer.parseInt(tmQL.getValueAt(i, 0).toString()),
                    Integer.parseInt(tmQL.getValueAt(i, 2).toString()),
                    tmQL.getValueAt(i, 4).toString());
            list.add(m);
        }
        return list;
    }
    
    public void hthiQL(List<QLDK> list, DefaultTableModel tm){
        tm.setRowCount(0);
        for(QLDK i:list){
            tm.addRow(new Object[] {i.getMaM(),getMHbyMa(i.getMaM()).getTen(),
                                    i.getMaS(),getSVbyMa(i.getMaS()).getTen(),
                                    i.getDate()
                });
        }
    }
