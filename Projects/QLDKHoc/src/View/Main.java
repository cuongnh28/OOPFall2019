    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        if(themQL){
            int maM=Integer.parseInt(jComboBox2.getSelectedItem().toString()),
                maS=Integer.parseInt(jComboBox3.getSelectedItem().toString());
            if(!tonTai(maM, maS)){
                if(getSoMH(maS)>=8){
                    JOptionPane.showMessageDialog(null, "Da dang ky hon 8 mon hoc.");
                    jComboBox3.requestFocus();
                    return;
                }
                else{
                    String tenMH=getMHbyMa(maM).getTen(),
                            tenSV=getSVbyMa(maS).getTen();
                    tmQL.addRow(new Object[] {maM,tenMH,maS,tenSV,jTextField8.getText()});
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Da ton tai.");
                jComboBox3.requestFocus();
                return;
            }
        }
        themQL=false;
    }//GEN-LAST:event_jButton12ActionPerformed

