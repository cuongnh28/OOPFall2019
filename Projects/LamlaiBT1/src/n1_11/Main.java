    public Sach getOject(){
        return new Sach(tm.getValueAt(getEditRow(),0).toString(),
                tm.getValueAt(getEditRow(),1).toString(),
                Double.parseDouble(tm.getValueAt(getEditRow(),2).toString()));
    }
    public void setObject(Sach s){
        tm.setValueAt(s.getMa(),getEditRow(),0);
        tm.setValueAt(s.getTen(),getEditRow(),1);
        tm.setValueAt(s.getGia(),getEditRow(),2);
    }
