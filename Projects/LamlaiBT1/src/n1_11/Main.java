    private boolean existedSach(String ma){
        for (int i = 0; i < tm.getRowCount(); i++) {
            if(tm.getValueAt(i,0).toString().equalsIgnoreCase(ma))
                return true;
        }
        return false;
    }
    public void add(Sach s){
        if(existedSach(s.getMa())){
            JOptionPane.showMessageDialog(null, "ma da ton tai!");
            return;
        }
        else
           tm.addRow(s.toObject());
    }
