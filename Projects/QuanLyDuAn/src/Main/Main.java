    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        if(themQL){
            int maD=Integer.parseInt(jComboBox2.getSelectedItem().toString()),
                maN=Integer.parseInt(jComboBox3.getSelectedItem().toString());
            if(!tonTai(maD, maN)){
                tmQL.addRow(new Object[] {maD,getDAbyMa(maD).getTen(),
                                maN,getNVbyMa(maN).getTen(),
            jTextField8.getText()});
            }
            else{
                JOptionPane.showMessageDialog(null, "Da ton tai.");
                jComboBox3.requestFocus();
                return;
            }
        }
        themQL=false;
    }//GEN-LAST:event_jButton12ActionPerformed

