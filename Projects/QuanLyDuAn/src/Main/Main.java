    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();
        jTextField1.setText(tmD.getValueAt(row, 0).toString());
        jTextField2.setText(tmD.getValueAt(row, 1).toString());
        jTextField3.setText(tmD.getValueAt(row, 3).toString());
        String kieu = tmD.getValueAt(row, 2).toString();
        for(int i=0;i<jComboBox1.getItemCount();i++){
            if(jComboBox1.getItemAt(i).toString().equals(kieu)){
                jComboBox1.setSelectedIndex(i);
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

