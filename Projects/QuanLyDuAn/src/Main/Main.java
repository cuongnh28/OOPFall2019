    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int row=jTable1.getSelectedRow();
        if(row<0 || row>=tmD.getRowCount()){
            JOptionPane.showMessageDialog(null, "Chon hang");
            return;
        }
        else{
            tmD.setValueAt(jTextField2.getText(), row, 1);
            tmD.setValueAt(jComboBox1.getSelectedItem().toString(), row, 2);
            tmD.setValueAt(jTextField3.getText(), row, 3);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

