    private void jButton14ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton14ActionPerformed
        if (themQL) {
            maBD = Integer.parseInt(jComboBox2.getSelectedItem().toString());
            maS = Integer.parseInt(jComboBox3.getSelectedItem().toString());
            if (!TonTaiMuon(maBD, maS)) {
                if(getDauSach(maBD)>=5) {
                    JOptionPane.showMessageDialog(null, "Da muon hon 5 dau sach.");
                    return;
                }
                else{
                    String tenBD=getBDByMa(maBD).getTen(),
                           tenS=getSachByMa(maS).getTen(),
                           ttrang=jTextField10.getText();
                    int sl=Integer.parseInt(jComboBox4.getSelectedItem().toString());
                    tmQL.addRow(new Object[] {maBD,tenBD,maS,tenS,sl,ttrang});
                }
            } else {
                JOptionPane.showMessageDialog(null, "Ban nay da muon cuon nay roi.");
                jComboBox3.requestFocus();
                return;
            }
        }
        themQL=false;
    }//GEN-LAST:event_jButton14ActionPerformed

