    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int ma = (Integer.parseInt(jTextField1.getText()));
        String ten=jTextField2.getText(),
                       tacgia = jTextField3.getText(),
                       chuyenNganh = jComboBox1.getSelectedItem().toString();
        String nam = jTextField4.getText(); // phai kiem tra xem nam co phai la so co 4 chu so hay khong.
        if(!nam.matches("\\d{4}")) {
            JOptionPane.showMessageDialog(null, "Nam phai la so co 4 chu so.");
            jTextField4.requestFocus();
            return;
        }
        else{
            int n=Integer.parseInt(jTextField4.getText()),
                sl=Integer.parseInt(jTextField5.getText());
            Sach s = new Sach(ma, ten, tacgia, chuyenNganh, n, sl);
            if(themS) {
                tmS.addRow(s.toObject());
                themS=false;
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

